public class If {
    public static void main(String[] args) {
        int myInt = 5;

        if (myInt < 10){
            System.out.println("yes, correct");
        } else if (myInt < 20){
            System.out.println("no, not correct");
        } else {
            System.out.println("no one is correct");
        }
    }
}
